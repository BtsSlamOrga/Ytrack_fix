namespace CSharpDiscovery.Quest01
{
    //Create a method AddTwoIntegers() that returns the sum of two integers.
    public class AddTwoIntegers_Exercice
    {
        public static int AddTwoIntegers(int a, int b)
        {
            // The sum of two integers is the result of adding them together.
            // you can do c = a + b; where c is the sum of a and b. And then return c.
            return a + b;
        }
    }
}